WorldMap.prototype.placePlayer = function (map) {
  for (let r in map) {
    if (!map.hasOwnProperty(r)) continue;
    for (let c in map[r]) {
      if (!map[r].hasOwnProperty(c)) continue;
      if (map[r][c] === 1) {
        map[r][c] = this.PLAYER;
        return map;
      }
    };
  };

  return map;
};

